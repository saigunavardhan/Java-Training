public class Calender {
	
	public static void main(String [] args) {
	String day = "Thursday";
	System.out.println("Are you free on "+ day+"?");
	
	switch(day) {
	case "Monday" :System.out.println("I am busy");
	break;
	
	case "Tuesday" :System.out.println("I am busy");
	break;
	
	case "Wednesday" :System.out.println("I am busy");
	break;
	
	case "Thursday" :System.out.println("I am busy");
	break;
	
	case "Friday" :System.out.println("I am not busy");
	break;
	
	
	}
}
	
}
